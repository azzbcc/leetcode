#define MAX_N 58
int pos = 4, data[MAX_N + 1] = { 0, 1, 1, 2 };

static int max(int a, int b) {
    return a > b ? a : b;
}

int integerBreak(int n) {
    if (n < pos) return data[n];

    for (; pos <= n; ++pos) {
        if (pos % 3 == 0) {
            data[pos] = max(data[pos - 3], pos - 3) * 3;
        } else {
            data[pos] = max(data[pos - 2], pos - 2) * 2;
        }
    }

    return data[n];
}