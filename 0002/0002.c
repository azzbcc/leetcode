/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     struct ListNode *next;
 * };
 */

struct ListNode *addTwoNumbers(struct ListNode *l1, struct ListNode *l2) {
    int val               = 0;
    struct ListNode *head = NULL, *tail = NULL, *tmp = NULL;

    while (l1 || l2 || val) {
        if (l1) val += l1->val;
        if (l2) val += l2->val;

        tmp       = calloc(1, sizeof(struct ListNode));
        tmp->val  = val % 10;
        tmp->next = NULL;

        if (head == NULL) {
            head = tmp;
        } else {
            tail->next = tmp;
        }
        tail = tmp;

        if (l1) l1 = l1->next;
        if (l2) l2 = l2->next;
        val /= 10;
    }

    return head;
}
