int *findDisappearedNumbers(int *nums, int size, int *returnSize) {
    int len = 0, *ans = malloc(size * sizeof(int));
    for (int i = 0; i < size; ++i) {
        if (nums[abs(nums[i]) - 1] > 0) nums[abs(nums[i]) - 1] *= -1;
    }
    for (int i = 0; i < size; ++i) {
        if (nums[i] < 0) continue;
        ans[len++] = i + 1;
    }
    *returnSize = len;
    return ans;
}
