char str[0x10000];
void dfs(struct TreeNode *root, char **s) {
    if (!root) return;
    *s += sprintf(*s, "%d", root->val);
    if (!root->left && !root->right) return;
    if (root->left) {
        *s += sprintf(*s, "(");
        dfs(root->left, s);
        *s += sprintf(*s, ")");
    } else {
        *s += sprintf(*s, "()");
    }
    if (root->right) {
        *s += sprintf(*s, "(");
        dfs(root->right, s);
        *s += sprintf(*s, ")");
    }
}
char *tree2str(struct TreeNode *root) {
    if (!root) return "";
    char *p = str;
    dfs(root, &p);
    return str;
}