int subarraySum(int *nums, int numsSize, int k) {
    int ans = 0;
    for (int i = 0; i < numsSize; ++i) {
        for (int j = i, sum = 0; j < numsSize; ++j) {
            sum += nums[j];
            ans += sum == k;
        }
    }
    return ans;
}