void nextPermutation(int *nums, int numsSize) {
    for (int pos = --numsSize - 1; pos >= 0; --pos) {
        int tmp = nums[pos];
        if (tmp >= nums[numsSize]) {
            for (int i = pos; i < numsSize; ++i) {
                nums[i] = nums[i + 1];
            }
            nums[numsSize] = tmp;
        } else {
            while (nums[--numsSize] > tmp) {}
            nums[pos]          = nums[numsSize + 1];
            nums[numsSize + 1] = tmp;
            return;
        }
    }
}
