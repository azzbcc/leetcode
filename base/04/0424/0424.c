#define WIDTH 26
int max(int a, int b) {
    return a > b ? a : b;
}
int characterReplacement(char *s, int k) {
    int left = 0, right = 0, count[WIDTH] = { 0 };
    for (int m = 0; s[right]; ++right) {
        m = max(m, ++count[s[right] - 'A']);
        if (right - left >= m + k) {
            count[s[left++] - 'A']--;
        }
    }
    return right - left;
}
