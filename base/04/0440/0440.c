int findKthNumber(int n, int k) {
    int ans = 1;
    for (k--; k;) {
        int step = 0;
        for (long first = ans, last = ans + 1; first <= n;
             step += last - first, first *= 10, last = fmin(last * 10, n + 1)) {}
        if (k >= step) {
            ans += 1, k -= step;
        } else {
            ans *= 10, k -= 1;
        }
    }
    return ans;
}
