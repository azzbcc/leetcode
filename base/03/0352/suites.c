static void check(SummaryRanges *ranges, int **target, size_t size) {
    int len = 0, *returnColumnSizes = NULL;
    int **ans = summaryRangesGetIntervals(ranges, &len, &returnColumnSizes);
    ck_assert_int_eq(len, size);
    for (int i = 0; i < len; ++i) {
        ck_assert_int_eq(returnColumnSizes[i], 2);
        ck_assert_mem_eq(ans[i], target[i], 2 * sizeof(int));
        free(ans[i]);
    }
    free(ans);
    free(returnColumnSizes);
}

START_TEST(test_official) {
    SummaryRanges *ranges = summaryRangesCreate();
    ck_assert_ptr_nonnull(ranges);

#define CHECK(arr) check(ranges, arr, LEN(arr))
    summaryRangesAddNum(ranges, 1);
    CHECK(((int *[]) { (int[]) { 1, 1 } }));

    summaryRangesAddNum(ranges, 3);
    CHECK(((int *[]) { (int[]) { 1, 1 }, (int[]) { 3, 3 } }));

    summaryRangesAddNum(ranges, 7);
    CHECK(((int *[]) { (int[]) { 1, 1 }, (int[]) { 3, 3 }, (int[]) { 7, 7 } }));

    summaryRangesAddNum(ranges, 2);
    CHECK(((int *[]) { (int[]) { 1, 3 }, (int[]) { 7, 7 } }));

    summaryRangesAddNum(ranges, 6);
    CHECK(((int *[]) { (int[]) { 1, 3 }, (int[]) { 6, 7 } }));
#undef CHECK

    summaryRangesFree(ranges);
}

