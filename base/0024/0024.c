/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     struct ListNode *next;
 * };
 */

struct ListNode *swapPairs(struct ListNode *l) {
    if (!l) return NULL;
    if (!l->next) return l;

    struct ListNode *next = l->next, *tail = swapPairs(l->next->next);
    next->next = l;
    l->next    = tail;

    return next;
}
